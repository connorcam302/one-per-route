import {
  SignIn,
  SignInButton,
  SignOutButton,
  useUser,
} from "@clerk/clerk-react";
import Head from "next/head";
import Link from "next/link";
import { RouterOutputs, api } from "~/utils/api";
import relativeTime from "dayjs/plugin/relativeTime";

export default function Home() {
  const user = useUser();

  const { data, isLoading } = api.runs.getAllActive.useQuery();

  const CreateRunWizard = () => {
    const { user } = useUser();
    if (!user) return null;

    return (
      <div className="flex">
        <img
          src={user.profileImageUrl}
          alt="Profile Image"
          className="h-16 w-16 rounded-full"
        />
      </div>
    );
  };

  type RunWithUser = RouterOutputs["runs"]["getAllActive"][number];
  const RunView = (props: RunWithUser) => {
    const { run, user } = props;
    console.log(run);
    return (
      <div key={run.id} className=" flex gap-3 border-b border-slate-400 p-4">
        <img
          src={user?.profileImageUrl}
          alt="Profile Image"
          className="h-16 w-16"
        />
        <div className="flex flex-col gap-3">
          <div className="flex">
            <span>{user?.username}</span>
          </div>
          <div className="flex">
            <span className="text-center">
              <img
                src={`/box-art/${run.game.name}.png`}
                className="h-20 w-20"
              />
              {run.game.name}
            </span>

            <img
              src={`/pkmn/full/${run.latestCatch?.toUpperCase()}.png`}
              className="h-16 w-16"
            />
          </div>
        </div>
      </div>
    );
  };

  if (isLoading) return <div>Loading...</div>;
  if (!data) return <div>Something went wrong.</div>;

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-screen justify-center">
        <div className="h-full w-full border-x border-slate-200 bg-red-800 md:max-w-2xl">
          <div className="flex border-b border-slate-200 p-4">
            {!user.isSignedIn && (
              <div className="flex justify-center">
                <SignInButton />
              </div>
            )}
            {user.isSignedIn && (
              <div className="flex w-full justify-between">
                <CreateRunWizard />
                <SignOutButton />
              </div>
            )}
          </div>
          <div className="flex flex-col">
            {data?.map((FullRun) => (
              <RunView {...FullRun} key={FullRun.run.id} />
            ))}
          </div>
        </div>
      </main>
    </>
  );
}
